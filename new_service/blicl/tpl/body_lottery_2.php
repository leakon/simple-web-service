	<div id="postmessage_12" class="t_msgfont">“顶呱刮”<span href="tag.php?name=%E5%A5%A5%E8%BF%90" onclick="tagshow(event)" class="t_tag">奥运</span>主题即开型<span href="tag.php?name=%E4%BD%93%E8%82%B2%E5%BD%A9%E7%A5%A8" onclick="tagshow(event)" class="t_tag">体育彩票</span>产品介绍<br />
<br />
 <br />
<br />
08奥运召开在即，为进一步宣扬奥运文化和奥运精神，扩大北京奥运会的影响力，经财政部、<span href="tag.php?name=%E5%9B%BD%E5%AE%B6%E4%BD%93%E8%82%B2%E6%80%BB%E5%B1%80" onclick="tagshow(event)" class="t_tag">国家体育总局</span>批准，并经第29届奥运会组委会授权许可，中国体育彩票新品牌“顶呱刮”即将推出以奥运为主题的系列即开型体育彩票。<br />
<br />
一、主题说明<br />
<br />
首批发行的“奥运主题系列”即开型体育彩票融合了丰富的奥运元素和中国文化特色，全系列共分7套，票面设计主题分别是：<br />
<br />
“勇争第一”涵盖了所有奥运比赛项目；<br />
<br />
“好运中国”将备受国人喜爱的北京奥运会吉祥物“福娃”搬上了票面；<br />
<br />
“奥运金牌”以独具创新的奥运奖牌“金镶玉”为设计元素，展现了中华文明与<span href="tag.php?name=%E5%A5%A5%E6%9E%97%E5%8C%B9%E5%85%8B" onclick="tagshow(event)" class="t_tag">奥林匹克</span>精神的完美结合；<br />
<br />
“激情梦想”以奥运火炬传递为载体，记录了奥运圣火在国内31个省、自治区、直辖市传递的路线和时间；<br />
<br />
“加油！中国队”从1984年<span href="tag.php?name=%E6%B4%9B%E6%9D%89%E7%9F%B6" onclick="tagshow(event)" class="t_tag">洛杉矶</span>奥运会到2004年雅典奥运会，记录了中国体育代表团在前6届奥运会上的表现。<br />
<br />
“奥运场馆”以北京奥运会比赛场馆及该场馆承办的比赛项目为主题，将奥林匹克运动与现代建筑设计融为一体。<br />
<br />
“奥运之城”涵盖了北京奥运会所有比赛城市。<br />
<br />
二、玩法介绍<br />
<br />
“奥运主题系列”彩票借鉴了国际先进的玩法元素，强调彩民的中奖体验。7套彩票分别采用了不同的游戏规则和奖级设置，单票面值分3元、5元、10元三种，单票中奖机会2－13次，最高中奖金额达25万元，平均中奖率高达25％。部分游戏规则及票样如下：<br />
<br />
