的感觉，因为保持平常心非常重要，自己还要继续上班，继续好好生活，幸福的生活要靠勤劳去创造。而那些奖金，他回去后要和家里人好好商量下用途，如果有余力的话，一定要帮助别的需要帮助的人。<span></span></font></span><span style="font-size: 9pt; line-height: 150%; font-family: 宋体"><font color="#000000">　　在采访的过程中，一口浓重山东口音的刘先生既朴实又爽快，他总在说，中不中奖都是为奥运和公益做贡献。其实每天都有很多人因为体彩中奖而突然感受幸福的喜悦，但这次奥运主题彩票却给彩民们带来了更多更丰富的内涵，因为这不但是中奖的幸福，更有一种和奥运和社会同呼吸共命运的喜悦，是一种参与的幸福，感受的幸福！<span></span></font></span><span style="font-size: 9pt; line-height: 150%; font-family: 宋体"><font color="#000000">　　奥运主题彩票的中奖快乐真的与众不同！<span>(</span>杨天婴<span>)</span></font></span></td></tr></tbody></table></div><span><font face="Times New Roman" size="3" color="#000000"> </font></span></td>
